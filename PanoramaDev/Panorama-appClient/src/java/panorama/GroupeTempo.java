package panorama;

/**
 *
 * @author clocal
 */
public class GroupeTempo 
{
private String numero_;
private String professeur_;
private String cours_;

    public GroupeTempo(String numero, String professeur, String cours) {
        numero_ = numero;
        professeur_ = professeur;
        cours_ = cours;
    }

    public String getNumero_() {
        return numero_;
    }

    public void setNumero_(String numero_) {
        this.numero_ = numero_;
    }

    public String getProfesseur_() {
        return professeur_;
    }

    public void setProfesseur_(String professeur_) {
        this.professeur_ = professeur_;
    }

    public String getCours_() {
        return cours_;
    }

    public void setCours_(String cours_) {
        this.cours_ = cours_;
    }

}
