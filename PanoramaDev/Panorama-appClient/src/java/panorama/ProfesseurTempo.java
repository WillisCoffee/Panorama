package panorama;

import java.io.Serializable;

/**
 *
 * @author clocal
 */
public class ProfesseurTempo implements Serializable
{
private String nom_;
private String prenom_;
private String noEmploye_;
private String departement_;

    public ProfesseurTempo(String nom, String prenom, String noEmploye, String departement) {
        nom_ = nom;
        prenom_ = prenom;
        noEmploye_ = noEmploye;
        departement_ = departement;
    }

    public String getNom_() {
        return nom_;
    }

    public void setNom_(String nom_) {
        this.nom_ = nom_;
    }

    public String getPrenom_() {
        return prenom_;
    }

    public void setPrenom_(String prenom_) {
        this.prenom_ = prenom_;
    }

    public String getNoEmploye_() {
        return noEmploye_;
    }

    public void setNoEmploye_(String noEmploye_) {
        this.noEmploye_ = noEmploye_;
    }

    public String getDepartement_() {
        return departement_;
    }

    public void setDepartement_(String departement_) {
        this.departement_ = departement_;
    }

}
