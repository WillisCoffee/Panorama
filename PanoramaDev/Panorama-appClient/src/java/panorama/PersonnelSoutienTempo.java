package panorama;

/**
 *
 * @author clocal
 */
public class PersonnelSoutienTempo 
{
private String nom_;
private String prenom_;
private String noEmploye_;
private String fonction_;

    public PersonnelSoutienTempo(String nom, String prenom, String noEmploye, String fonction) {
        nom_ = nom;
        prenom_ = prenom;
        noEmploye_ = noEmploye;
        fonction_ = fonction;
    }

    public String getNom_() {
        return nom_;
    }

    public void setNom_(String nom_) {
        this.nom_ = nom_;
    }

    public String getPrenom_() {
        return prenom_;
    }

    public void setPrenom_(String prenom_) {
        this.prenom_ = prenom_;
    }

    public String getNoEmploye_() {
        return noEmploye_;
    }

    public void setNoEmploye_(String noEmploye_) {
        this.noEmploye_ = noEmploye_;
    }

    public String getFonction_() {
        return fonction_;
    }

    public void setFonction_(String fonction_) {
        this.fonction_ = fonction_;
    }

}
